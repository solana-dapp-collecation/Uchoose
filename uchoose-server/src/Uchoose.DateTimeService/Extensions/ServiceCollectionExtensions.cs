using Microsoft.Extensions.DependencyInjection;
using Uchoose.DateTimeService.Interfaces;
using Uchoose.Utils.Extensions;

namespace Uchoose.DateTimeService.Extensions
{
    /// <summary>
    /// Методы расширения для <see cref="IServiceCollection"/>.
    /// </summary>
    public static class ServiceCollectionExtensions
    {
        /// <summary>
        /// Добавить сервис для работы с датой и временем.
        /// </summary>
        /// <param name="services"><see cref="IServiceCollection"/>.</param>
        /// <returns>Возвращает <see cref="IServiceCollection"/>.</returns>
        public static IServiceCollection AddDateTimeService(this IServiceCollection services)
        {
            return services
                .AddScopedInfrastructureService<IDateTimeService, DateTimeService>();
        }
    }
}